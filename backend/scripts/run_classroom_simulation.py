async def main():
    parser = argparse.ArgumentParser(description="AP Classroom Discussion Simulation")
    parser.add_argument("--config", type=str, required=True, help="AP config JSON path")
    parser.add_argument("--profiles", type=str, default=None, help="Profiles JSON path (if already generated)")
    parser.add_argument("--generate-profiles", action="store_true", help="Generate profiles from config first")
    parser.add_argument("--no-llm", action="store_true", help="Skip LLM for profile generation")
    parser.add_argument("--no-wait", action="store_true", help="Exit immediately after simulation")
    parser.add_argument("--output-dir", type=str, default=None, help="Output directory (default: same as config)")
    args = parser.parse_args()

    global _shutdown_event
    _shutdown_event = asyncio.Event()

    if not os.path.exists(args.config):
        print(f"Error: Config not found: {args.config}")
        sys.exit(1)

    output_dir = args.output_dir or os.path.dirname(os.path.abspath(args.config))
    os.makedirs(output_dir, exist_ok=True)

    profiles_path = args.profiles or os.path.join(output_dir, "reddit_profiles.json")

    # Generate profiles if requested or if they don't exist
    if args.generate_profiles or not os.path.exists(profiles_path):
        print("\n  Generating student profiles...\n")
        # Import here to avoid circular deps
        sys.path.insert(0, os.path.join(_scripts_dir, ".."))
        from app.services.student_profile_generator import StudentProfileGenerator

        gen = StudentProfileGenerator()

        def progress(cur, total, msg):
            print(f"    [{cur}/{total}] {msg}")

        profiles = gen.generate_profiles(
            config_path=args.config,
            use_llm=not args.no_llm,
            progress_callback=progress,
        )
        gen.save_profiles(profiles, profiles_path)
        print(f"\n  ✓ {len(profiles)} profiles saved to {profiles_path}\n")

    setup_logging(os.path.join(output_dir, "log"))

    runner = ClassroomSimulationRunner(
        config_path=args.config,
        profiles_path=profiles_path,
        wait_for_commands=not args.no_wait,
    )
    await runner.run()


def setup_signal_handlers():
    def handler(signum, frame):
        global _cleanup_done
        print(f"\n  Signal {signum} received, shutting down...")
        if not _cleanup_done:
            _cleanup_done = True
            if _shutdown_event:
                _shutdown_event.set()
        else:
            sys.exit(1)

    signal.signal(signal.SIGTERM, handler)
    signal.signal(signal.SIGINT, handler)


if __name__ == "__main__":
    setup_signal_handlers()
    try:
        asyncio.run(main())
    except KeyboardInterrupt:
        print("\n  Interrupted")
    except SystemExit:
        pass
